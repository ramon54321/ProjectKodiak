package ramonbrand.kodiak.logging

object KASCIColors {
    val RESET = "\u001B[0m"
    val BLACK = "\u001B[30m"
    val RED = "\u001B[31m"
    val GREEN = "\u001B[32m"
    val YELLOW = "\u001B[33m"
    val BLUE = "\u001B[34m"
    val MAGENTA = "\u001B[35m"
    val CYAN = "\u001B[36m"
    val WHITE = "\u001B[37m"

    val BLACK_BRIGHT = "\u001B[30;1m"
    val RED_BRIGHT = "\u001B[31;1m"
    val GREEN_BRIGHT = "\u001B[32;1m"
    val YELLOW_BRIGHT = "\u001B[33;1m"
    val BLUE_BRIGHT = "\u001B[34;1m"
    val MAGENTA_BRIGHT = "\u001B[35;1m"
    val CYAN_BRIGHT = "\u001B[36;1m"
    val WHITE_BRIGHT = "\u001B[37;1m"
}
